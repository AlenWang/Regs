var Regs = {
  //数字
  num: /^[0-9]*$/,

  //字母
  en: /^[a-zA-Z]+$/,

  //汉字
  cn: /^[\u4e00-\u9fa5]{0,}$/,

  //英文、数字
  numEn: /^[A-Za-z0-9]+$/,

  //数字、字母、下划线
  numEn_: /^\w+$/,

  //数字、字母、下划线、汉字
  numEnNum_: /^[\u4E00-\u9FA5A-Za-z0-9_]+$/,

  //Email邮箱
  email: /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,

  //qq号
  qq: /[1-9][0-9]{4,}/
}